#!/usr/bin/env node
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.join(__dirname, '..');
const logosDir = path.join(projectRoot, 'public', 'client logos');
const dataDir = path.join(projectRoot, 'src', 'data');
const outJson = path.join(dataDir, 'client-logos.json');
const outTs = path.join(dataDir, 'client-logos.ts');

const IMG_EXT = /\.(png|jpe?g|svg|gif|webp)$/i;

// Fallback when public/client logos is missing or empty – logos load from ImageKit (client-logos folder)
const FALLBACK_CLIENT_LOGOS = [
  '1024px-Cisco_logo.svg_.png',
  '98b4e8_d71ea6626990460c8891b856b61618bamv2.webp',
  'Acc_Logo_Black_Purple_RGB.png',
  'Bank_of_Baroda_logo-3.svg',
  'Tesco_Logo.svg_.png',
  'Zomato-Logo.png',
  'basf_logo.svg',
  'download-17.png',
  'huawei_  logo.svg',
  "john_deere logo.svg",
  "kellogg's_logo.svg",
  'kfc_logo.svg',
  'philips_logo.svg',
  'pwc_logo.svg',
  'red_bull logo.svg',
];

function listLogos() {
  let names = [];
  try {
    if (fs.existsSync(logosDir)) {
      names = fs.readdirSync(logosDir)
        .filter((name) => IMG_EXT.test(name) && !name.startsWith('.'))
        .sort();
    }
  } catch (_) {
    // ignore
  }
  if (names.length === 0) {
    names = FALLBACK_CLIENT_LOGOS;
  }
  if (!fs.existsSync(dataDir)) {
    fs.mkdirSync(dataDir, { recursive: true });
  }
  fs.writeFileSync(outJson, JSON.stringify(names, null, 2), 'utf8');
  const tsContent = `// Auto-generated by scripts/list-client-logos.mjs – do not edit\n\nexport const clientLogos: string[] = ${JSON.stringify(names)};\n`;
  fs.writeFileSync(outTs, tsContent, 'utf8');
  console.log('Listed', names.length, 'client logos →', outJson, '+', outTs);
}

listLogos();
