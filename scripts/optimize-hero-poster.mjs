#!/usr/bin/env node
/**
 * Compresses hero poster PNG to WebP for LCP optimization (target <120kb).
 * Updates index.html preload and writes hero-poster-config.ts for HeroSection.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.join(__dirname, '..');
const srcPath = path.join(projectRoot, 'public', 'best garment factory in bangalore.png');
const outPath = path.join(projectRoot, 'public', 'hero-poster.webp');
const indexPath = path.join(projectRoot, 'index.html');
const configPath = path.join(projectRoot, 'src', 'hero-poster-config.ts');

const PNG_FALLBACK = '/best%20garment%20factory%20in%20bangalore.png';

async function run() {
  let usePath = PNG_FALLBACK;
  try {
    const sharp = (await import('sharp')).default;
    if (!fs.existsSync(srcPath)) {
      console.warn('Hero poster not found, skipping optimization');
    } else {
      await sharp(srcPath)
        .resize(1920, 1080, { fit: 'inside', withoutEnlargement: true })
        .webp({ quality: 42, effort: 6 })
        .toFile(outPath);
      const stat = fs.statSync(outPath);
      const kb = (stat.size / 1024).toFixed(1);
      console.log(`Hero poster optimized: hero-poster.webp (${kb} KB)`);
      usePath = '/hero-poster.webp';
    }
  } catch (err) {
    if (err.code === 'ERR_MODULE_NOT_FOUND' && err.message?.includes('sharp')) {
      console.warn('sharp not installed. Run: npm install sharp --save-dev for hero image optimization.');
    } else {
      console.warn('Hero optimization failed:', err.message);
    }
  }
  const preloadHref = usePath.startsWith('/') ? usePath : '/' + usePath;
  const tsContent = `// Auto-generated by scripts/optimize-hero-poster.mjs – do not edit
export const HERO_POSTER = '${preloadHref}';
`;
  fs.mkdirSync(path.dirname(configPath), { recursive: true });
  fs.writeFileSync(configPath, tsContent, 'utf8');
  if (fs.existsSync(indexPath)) {
    let html = fs.readFileSync(indexPath, 'utf8');
    const preloadLine = `<link rel="preload" as="image" href="${preloadHref}" fetchpriority="high" />`;
    html = html.replace(
      /<link\s+rel="preload"\s+as="image"\s+href="[^"]*"\s*\/?>/,
      preloadLine
    );
    fs.writeFileSync(indexPath, html, 'utf8');
  }
}

run();
